#!/usr/bin/env bash
set -e
PACKAGE_LIST_LOCATION=${PACKAGE_LIST_LOCATION:-package-lists}
DESIRED_PACKAGE_LISTS_ARG=$1

PACKAGES_TMPFILE=$(mktemp)

for PACKAGE_LIST in ${DESIRED_PACKAGE_LISTS_ARG//,/ }
do 
    cat $PACKAGE_LIST_LOCATION/$PACKAGE_LIST  >> $PACKAGES_TMPFILE || exit 4
done

sort -u $PACKAGES_TMPFILE

rm $PACKAGES_TMPFILE

